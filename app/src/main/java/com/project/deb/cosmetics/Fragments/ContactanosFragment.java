/**
 * A simple {@link Fragment} subclass.
 */
public class ContactanosFragment extends Fragment implements OnMapReadyCallback{

    private final String TAG = "ContactanosFragment";

    MapView mapView;
    GoogleMap googleMap;
    View view;

    public ContactanosFragment() {
        // Required empty public constructor
    }


    @Override
    public View onCreateView(LayoutInflater inflater, ViewGroup container,
                             Bundle savedInstanceState) {
        // Inflate the layout for this fragment
        view =  inflater.inflate(R.layout.fragment_contactanos, container, false);

        return view;
    }

    @Override
    public void onViewCreated(View view, Bundle savedInstanceState) {
        super.onViewCreated(view, savedInstanceState);

        mapView = (MapView) view.findViewById(R.id.map);
        if(mapView != null){
            mapView.onCreate(null);
            mapView.onResume();
            mapView.getMapAsync(this);
        }
    }

    @Override
    public void onMapReady(GoogleMap googleMap) {

        MapsInitializer.initialize(getContext());



        LatLng ll = new LatLng(-12.1064917,-76.9674603);

        //googleMap.setMapType(GoogleMap.MAP_TYPE_NORMAL);

        try {
            // Customise the styling of the base map using a JSON object defined
            // in a raw resource file.
            boolean success = googleMap.setMapStyle(
                    MapStyleOptions.loadRawResourceStyle(
                            this.getContext(), R.raw.style_json));

            if (!success) {
                Log.e(TAG, "Style parsing failed.");
            }
        } catch (Resources.NotFoundException e) {
            Log.e(TAG, "Can't find style. Error: ", e);
        }

        googleMap.addMarker(new MarkerOptions().position(ll));

        CameraPosition pos = CameraPosition.builder().target(ll).zoom(16).build();
        googleMap.moveCamera(CameraUpdateFactory.newCameraPosition(pos));
    }
}
